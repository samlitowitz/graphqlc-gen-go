type Generator struct {
	*echo.Generator

	config   *Config
	genFiles map[string]bool
	typeMap  map[string]*ast.Ident
}

func New() *Generator {
	g := new(Generator)
	g.Generator = echo.New()
	g.LogPrefix = "graphqlc-gen-go"
	return g
}

func (g *Generator) GenerateSchemaFiles() {}

func (g *Generator) CommandLineArguments(parameter string) {
	g.Generator.CommandLineArguments(parameter)

	for k, v := range g.Param {
		switch k {
		case "config":
			config, err := LoadConfig(v)
			if err != nil {
				g.Error(err)
			}
			g.config = config
		}
	}
	if g.config == nil {
		g.Fail("a configuration must be provided")
	}
}

func (g *Generator) GenerateAllFiles() {
	g.buildGenFilesMap()
	g.generateTypeDefinitions()
}

func (g *Generator) generateTypeDefinitions() {
	g.buildTypeMapBase()
	// build scalar ide
	for _, fd := range g.Request.GraphqlFile {
		if _, ok := g.genFiles[fd.Name]; !ok {
			continue
		}
		f := &ast.File{
			Name: ast.NewIdent(g.config.Package),
			Scope: &ast.Scope{
				Objects: make(map[string]*ast.Object),
			},
			Doc: &ast.CommentGroup{
				List: []*ast.Comment{},
			},
			Comments: []*ast.CommentGroup{},
		}

		for _, def := range fd.Enums {
			f.Decls = append(f.Decls, &ast.GenDecl{
				Tok: token.TYPE,
				Specs: []ast.Spec{
					&ast.TypeSpec{
						Name: ast.NewIdent(def.Name),
						Type: ast.NewIdent("int64"),
					},
				},
				Rparen: 0,
			})
			g.typeMap[def.Name] = ast.NewIdent(def.Name)
			if len(def.Values) == 0 {
				continue
			}
			valueSpecs := []ast.Spec{
				&ast.ValueSpec{
					Names:  []*ast.Ident{ast.NewIdent(def.Name + "_" + def.Values[0].Value)},
					Type:   g.typeMap[def.Name],
					Values: []ast.Expr{ast.NewIdent("iota")},
				},
			}
			for _, value := range def.Values[1:] {
				valueSpecs = append(valueSpecs, &ast.ValueSpec{
					Names: []*ast.Ident{ast.NewIdent(def.Name + "_" + value.Value)},
				})
			}
			f.Decls = append(f.Decls, &ast.GenDecl{
				Tok:    token.CONST,
				Lparen: 0,
				Specs:  valueSpecs,
				Rparen: 0,
			})
		}



		//for _, objDef := range fd.Objects {
		//	typeMap[objDef.Name] = &ast.GenDecl{
		//		Tok:    token.TYPE,
		//		Specs:  []ast.Spec{
		//			&ast.TypeSpec{
		//				Name:    nil,
		//				Assign:  0,
		//				Type:    nil,
		//				Comment: nil,
		//			},
		//		},
		//	}
		//}

		fset := token.NewFileSet()
		//calculatePositions(f, fset.Base())

		g.Write([]byte("// DO NOT MODIFY!!! This code is generated by graphqlc-gen-go.\n"))
		err := printer.Fprint(g, fset, f)
		if err != nil {
			g.Error(err, "failed to print AST")
		}
		// content := g.String()
		// g.Error(fmt.Errorf("%# v\n%s\n", pretty.Formatter(f), content), "stuff")

		filename := fmt.Sprintf("%s_generated.go", strings.TrimSuffix(fd.Name, path.Ext(fd.Name)))
		g.Response.File = append(g.Response.File, &graphqlc.CodeGeneratorResponse_File{
			Name:    g.config.Output + filename,
			Content: g.String(),
		})
	}
}

func calculatePositions(f *ast.File, pos int) {
	offset := pos

	// Package level comments
	for _, comment := range f.Doc.List {
		comment.Slash = token.Pos(offset)
		offset += len(comment.Text) + 2 // \n
	}

	// Package declaration and name
	f.Package = token.Pos(offset)
	offset += len("package ")
	f.Name.NamePos = token.Pos(offset)
	offset += len(f.Name.Name) + 3 // \n
}

