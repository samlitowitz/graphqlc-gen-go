func (g *Generator) buildGenFilesMap() {
	g.genFiles = make(map[string]bool)
	for _, file := range g.Request.FileToGenerate {
		g.genFiles[file] = true
	}
}

func (g *Generator) buildTypeMapBase() {
	g.typeMap = map[string]*ast.Ident{
		"Int":     ast.NewIdent("string"),
		"Float":   ast.NewIdent("float"),
		"String":  ast.NewIdent("string"),
		"Boolean": ast.NewIdent("bool"),
		"ID":      ast.NewIdent("string"),
	}
}

func (g *Generator) addMappedScalarsToTypeMap() {
	for graphqlType, goDef := range g.config.ScalarMap {
		ident := ast.NewIdent(goDef.Type)
		g.typeMap[graphqlType] = ast.NewIdent()
	}
}
